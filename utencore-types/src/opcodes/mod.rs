//! UtenCore opcode definitions — bytecode format v3.
//! Removed single-line library-level ops: float math, bitwise advanced,
//! debug, GC control, JIT, and a few single-line stubs.

use bitflags::bitflags;
use std::fmt;
pub mod info;
pub use info::opcode_info;

bitflags! {
    #[derive(Debug, Clone, Copy, PartialEq, Eq)]
    pub struct OpFlags: u16 {
        const HAS_LOCAL     = 0x0001;
        const IS_JUMP       = 0x0002;
        const HAS_STRING    = 0x0004;
        const HAS_FUNC      = 0x0008;
        const HAS_MODULE    = 0x0010;
        const HAS_TYPE      = 0x0020;
        const MAY_GC        = 0x0040;
        const IMM32         = 0x0080;
        const IMM64         = 0x0100;
        const TERMINATOR    = 0x0200;
        const HAS_CONST     = 0x0400;
        const HAS_GLOBAL    = 0x0800;
    }
}

#[derive(Clone)]
pub struct OpcodeInfo {
    pub mnemonic: &'static str,
    pub operand_size: u8,
    pub flags: OpFlags,
    pub stack_effect: i8,
}

fn opi(mnemonic: &'static str, operand_size: u8, flags: OpFlags, stack_effect: i8) -> OpcodeInfo {
    OpcodeInfo { mnemonic, operand_size, flags, stack_effect }
}

#[derive(Debug, Clone, Copy, PartialEq, Eq, Hash)]
#[repr(u8)]
pub enum Opcode {
    // 0x00-0x0F: Stack (all 16)
    Nop=0x00,PushNil=0x01,PushTrue=0x02,PushFalse=0x03,PushI32=0x04,PushI64=0x05,
    PushF32=0x06,PushF64=0x07,PushString=0x08,PushConst=0x09,Dup=0x0A,DupN=0x0B,
    Swap=0x0C,Pop=0x0D,PopN=0x0E,Rot=0x0F,

    // 0x10-0x1F: Integer (kept — multi-line dispatch)
    Add=0x10,Sub=0x11,Mul=0x12,Div=0x13,Mod=0x14,Neg=0x15,
    Inc=0x16,Dec=0x17,Abs=0x18,Pow=0x19,
    CheckedAdd=0x1A,CheckedSub=0x1B,CheckedMul=0x1C,
    SaturatingAdd=0x1D,SaturatingSub=0x1E,WrappingAdd=0x1F,

    // 0x20-0x2F: Float (basic only)
    FAdd=0x20,FSub=0x21,FMul=0x22,FDiv=0x23,FMod=0x24,FNeg=0x25,
    // 0x26-0x2F: reserved (advanced float: sqrt, trig, round — use library)

    // 0x30-0x3F: Bitwise (basic)
    BitAnd=0x30,BitOr=0x31,BitXor=0x32,BitNot=0x33,Shl=0x34,Shr=0x35,
    // 0x36-0x3F: reserved (advanced bitwise — use library)

    // 0x40-0x4D: Comparison & Logical
    Eq=0x40,Ne=0x41,Lt=0x42,Le=0x43,Gt=0x44,Ge=0x45,
    Cmp=0x46,Is=0x47,IsNot=0x48,In=0x49,NotIn=0x4A,
    And=0x4B,Or=0x4C,Not=0x4D,
    // 0x4E-0x4F: reserved

    // 0x50-0x5F: Type & Conversion (all multi-line)
    TypeOf=0x50,ToI32=0x52,ToI64=0x53,ToF32=0x54,ToF64=0x55,
    ToBool=0x56,ToString=0x57,Cast=0x58,BitCast=0x59,
    EnumCreate=0x5A,EnumMatch=0x5B,CheckIndex=0x5C,CheckType=0x5D,
    TypeAssert=0x5E,Unreachable=0x5F,

    // 0x60-0x6F: Control Flow
    Jump=0x60,JumpIfFalse=0x61,JumpIfTrue=0x62,JumpIfEq=0x63,JumpIfNe=0x64,
    JumpTable=0x65,ForPrep=0x66,ForStep=0x67,Loop=0x68,Switch=0x69,
    MatchCheck=0x6A,Bind=0x6B,GetIter=0x6C,Next=0x6D,
    // 0x6E-0x6F: reserved

    // 0x70-0x7F: Call & Return
    Call=0x70,CallValue=0x71,CallMethod=0x72,TailCall=0x73,
    TailCallValue=0x74,Apply=0x77,Return=0x78,ReturnValue=0x79,
    ReturnMultiple=0x7A,MakeClosure=0x7B,
    Capture=0x7C,LoadUpvalue=0x7D,StoreUpvalue=0x7E,Curry=0x7F,
    // 0x75-0x77: reserved

    // 0x80-0x8F: Variables
    LoadLocal=0x80,StoreLocal=0x81,LoadGlobal=0x84,StoreGlobal=0x85,
    AllocFrame=0x88,LoadArg=0x89,This=0x8E,ArgCount=0x8F,
    // 0x82-0x83, 0x86-0x87, 0x8A-0x8D: reserved

    // 0x90-0x9F: Array (all kept)
    NewArray=0x90,ArrayLen=0x91,ArrayGet=0x92,ArraySet=0x93,
    ArrayPush=0x94,ArrayPop=0x95,
    ArrayUnshift=0x96,ArrayShift=0x97,ArrayInsert=0x98,ArrayRemove=0x99,
    ArraySlice=0x9A,ArrayConcat=0x9B,ArrayContains=0x9C,ArrayIndexOf=0x9D,
    ArraySort=0x9E,ArrayReverse=0x9F,

    // 0xA0-0xAF: Map & Set (all kept)
    NewMap=0xA0,MapGet=0xA1,MapSet=0xA2,MapDel=0xA3,
    MapContains=0xA4,MapKeys=0xA5,MapLen=0xA6,
    NewSet=0xA7,SetAdd=0xA8,SetRemove=0xA9,SetContains=0xAA,SetLen=0xAB,
    SetUnion=0xAC,SetIntersect=0xAD,NewRange=0xAE,Tuple=0xAF,

    // 0xB0-0xBF: String & Regex (all kept)
    StrConcat=0xB0,StrLen=0xB1,StrGet=0xB2,StrSub=0xB3,
    StrContains=0xB4,StrIndexOf=0xB5,StrReplace=0xB6,StrSplit=0xB7,StrJoin=0xB8,
    StrCmp=0xBC,RegexCompile=0xBE,RegexMatch=0xBF,
    // 0xB9-0xBB, 0xBD: reserved

    // 0xC0-0xCF: OOP (all kept)
    NewNamespace=0xC0,NewClass=0xC1,NewObject=0xC2,
    ClassAddMethod=0xC3,ClassAddField=0xC4,ClassSetParent=0xC5,
    GetAttr=0xC6,SetAttr=0xC7,HasAttr=0xC8,InstanceOf=0xC9,
    GetField=0xCA,SetField=0xCB,
    GetFieldIdx=0xCC,SetFieldIdx=0xCD,HasField=0xCE,InitStruct=0xCF,

    // 0xD0-0xDF: Functional & Coroutine (all kept)
    Cons=0xD0,Car=0xD1,Cdr=0xD2,List=0xD3,IsList=0xD4,
    MapFn=0xD5,FilterFn=0xD6,ReduceFn=0xD7,Compose=0xD8,Delay=0xD9,Force=0xDA,
    MakeCoroutine=0xDB,CoroutineStatus=0xDC,CoroutineYield=0xDD,
    ResumeWith=0xDE,Continuation=0xDF,

    // 0xE0-0xEF: CIB & Module (all kept)
    CibLoad=0xE0,CibSym=0xE1,CibCall=0xE2,CibWrap=0xE3,CibUnwrap=0xE4,
    CibStrToC=0xE6,CibStrFromC=0xE7,CibSizeOf=0xE8,
    CibCallTyped=0xE9,CibLoadInterface=0xEA,
    Import=0xEC,ImportFunc=0xED,ImportValue=0xEE,Export=0xEF,
    // 0xE5, 0xEB: reserved

    // 0xF0-0xFF: GC, Alloc, Debug (all kept)
    Alloc=0xF0,GcCollect=0xF1,GcPin=0xF2,GcUnpin=0xF3,GcStats=0xF4,
    WriteBarrier=0xF5,GcSetThreshold=0xF6,
    JitCompile=0xF7,JitInvalidate=0xF8,JitStat=0xF9,
    Print=0xFA,Trace=0xFB,Breakpoint=0xFC,
    Halt=0xFE,Raise=0xFF,
    // 0xFD: reserved
}

